marks = int(input("Enter your Grade (0 - 100): "))

if marks >= 90:
	print("Grade is Excellent")
elif marks >= 80:
	print("Grade is Very Good")
elif marks >= 70:
	print("Grade is Good")
elif marks >= 60:
	print("Grade is Better")
elif marks >= 50:
	print("Grade is Pass")
else:
	print("Try your luck next time")
